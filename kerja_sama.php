              <p>(Seoul, Itjen Kemenedikbudristek) – Kerja sama Indonesia dengan negara-negara tetangganya dalam bidang pendidikan semakin intens berjalan. Sebelumnya ada Institut Pertanian Bogor (IPB) yang melakukan kerja sama dengan Universitas di Shizuoka, Jepang. Kini ada empat universitas negeri di Indonesia yang akan bekerja sama dengan Seoul Nasional University (SNU). Empat universitas tersebut terdiri dari Universitas Mataram, Universitas Padjadjaran, Universitas Negeri Padang, dan Universitas  Malikussaleh. Ke empat pimpinan universitas tersebut bertemu dengan Rektor SNU, Honglim Ryu pada Rabu, (08/03/2023) di Kantor Pusat SNU, Seoul, Korea Selatan. Agenda yang dibahas dalam pertemuan ini adalah perihal konsorsium kerja sama riset dan joint working group pendidikan tinggi negeri di Indonesia dan Korea.</p><br>

              <p>“Selain program akademik dan kegiatan penelitian, kami di Universitas Mataram juga mendedikasikan program kampus untuk mendorong kelestarian lingkungan sebagai pelayanan masyarakat,” kata Rektor Universitas Mataram Bambang Hari Kusumo selaku pimpinan delegasi perguruan tinggi.</p><br>

              <p>Selain dari kerja sama yang telah dibicarakan, Rektor Bambang juga menyinggung adanya peluang berkolaborasi dalam hal penelitian bersama, pengembangan kompetensi peneliti, pertukaran mahasiswa, pengiriman dosen untuk melanjutkan studi di SNU, serta pertukaran mahasiswa Korea ke Indonesia.</p><br>

              <p>Dalam kesempatan yang sama, Honglim Ryu menyampaikan apresiasinya atas minat dan usaha Indonesia  terkait kerja sama bidang teknik, pendidikan, dan dan ilmu olahraga, serta berbagai kerja sama internasional lainnya, di mana saat ini juga merupakan prioritas program SNU di Indonesia.</p><br>

              <p>“Kami berharap forum pertukaran bidang-bidang strategis bagi kedua negara akan diadakan dengan melibatkan peneliti SNU maupun peneliti Indonesia pada bidang yang relevan,” urai Honglim Ryu.</p><br>

              <p>Selain itu, Honglim Ryu turut mengangkat dan memuji  pembentukan S-Cube Center di Universitas Mataram pada 1 Februari, yang pendiriannya turut didukung oleh SNU melalui kolaborasi dengan Seoul National Pusat R&DB Global.</p><br>

              <p>Atase Pendidikan dan Kebudayaan (Atdikbud) Kedutaan Besar Republik Indonesia (KBRI), Gogot Suharwoto dalam kesempatan ini juga menyampaikan apresiasi kepada SNU yang setiap tahun memberikan beasiswa bagi mahasiswa asing, termasuk Indonesia.</p><br>

              <p>“Kementerian Pendidikan, Kebudayaan, Riset, dan Teknologi (Kemendikbudristek) juga sedang mengirimkan 1.000 mahasiswa Indonesia ke kampus terbaik di dunia selama satu semester sebagai bagian dari program Kampus Merdeka Merdeka Belajar. Kampus Korea pada tahun 2022 menjadi tujuan favorit calon penerima program Indonesian International Student Mobility Award (IISMA),” imbuh Gogot Suharwoto.</p><br>

